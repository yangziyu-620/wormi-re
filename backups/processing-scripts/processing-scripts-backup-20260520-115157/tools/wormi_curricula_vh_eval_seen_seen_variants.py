"""Eval-only curricula for seen-seen VirtualHome split diagnostics.

This keeps the trained WorMI/world-model paths and retrieval settings aligned
with tools/wormi_curricula_vh.py, but replaces Table-1 test curricula with
larger seen-task/seen-scene variants generated by
tools/build_virtualhome_eval_variants.py.
"""

import os
from pathlib import Path

from wormi.curricula import WorldModel, WorMICurricula, WorMICurriculum
from wormi.model import WorMIIntegrateMethod
from wormi.trainer import WorMITrainerConfig

DATA_DISK = Path(os.environ.get("WORMI_DATA_DISK", "/root/autodl-tmp"))
DATASET_ROOT = Path(
    os.environ.get("WORMI_VH_DATA_ROOT", DATA_DISK / "wormi-data" / "virtualhome")
)
WORLD_CKPT_ROOT = Path(
    os.environ.get(
        "WORMI_WORLD_VH_OUTPUT_DIR",
        DATA_DISK / "wormi-checkpoints" / "world-vh-semantic1023-rerun",
    )
)
OUTPUT_DIR = Path(
    os.environ.get(
        "WORMI_VH_OUTPUT_DIR",
        DATA_DISK / "wormi-checkpoints" / "wormi-vh-semantic1023-rerun",
    )
)

BASE_MODEL = "unsloth/Llama-3.2-3B-Instruct"
BASE_CONNECTIONS = [13, 27]
WORLD_CONNECTIONS = [7, 15]
SCENES = [f"scene_{i}" for i in range(6)]

EVAL_DIRS = [
    "eval_col_1_seen_seen",
    "eval_col_1_seen_seen_full",
    "eval_col_1_seen_seen_balanced_6",
]
DATASETS = [str(DATASET_ROOT / s) for s in SCENES] + [
    str(DATASET_ROOT / d) for d in EVAL_DIRS
]

test_trainer_args = WorMITrainerConfig(max_steps=0, batch_size=1)

curricula = WorMICurricula(
    name="wormi-vh-n6",
    output_dir=OUTPUT_DIR,
    base_model=BASE_MODEL,
    connections=BASE_CONNECTIONS,
    method=WorMIIntegrateMethod.WORLD_WISE_ATTENTION,
    num_heads=8,
    self_attention=False,
    meta_learning=True,
    meta_learning_rate=0.1,
    num_iterations=8,
    world_models=[
        WorldModel(
            model_name=str(WORLD_CKPT_ROOT / s / "last"),
            connections=WORLD_CONNECTIONS,
        )
        for s in SCENES
    ],
    datasets=DATASETS,
    train_curricula=[],
    test_curricula=[
        WorMICurriculum(
            name="col_1_seen_seen_original_heldout",
            target_world_models=[0, 1, 2],
            datasets=[6],
            trainer_args=test_trainer_args,
        ),
        WorMICurriculum(
            name="col_1_seen_seen_full_86",
            target_world_models=[0, 1, 2],
            datasets=[7],
            trainer_args=test_trainer_args,
        ),
        WorMICurriculum(
            name="col_1_seen_seen_balanced_24",
            target_world_models=[0, 1, 2],
            datasets=[8],
            trainer_args=test_trainer_args,
        ),
    ],
    test_continuously=False,
    sentence_embedding_model="sentence-transformers/all-MiniLM-L6-v2",
    retrieval_k=3,
    prototype_size=15,
)
